    private void SMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SMouseDragged
        L.setText("Good");
        int feed=S.getValue();
        if(feed>=0&&feed<=20)
            L.setText("Very Poor");
        else if(feed>20&&feed<=40)
            L.setText("Satisfactory");
        else if(feed>40&&feed<=60)
            L.setText("Good");
        else if(feed>60&&feed<=80)
            L.setText("Very Good");
        else
            L.setText("Excellent!");
    }//GEN-LAST:event_SMouseDragged

